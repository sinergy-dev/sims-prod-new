    public function store(Request $request)
    {
    	$tambah = new DokRepo();
    	$tambah->nama 		= $request['nama_dok'];
    	$tambah->deskripsi  = $request['deskripsi'];
    	$file               = $request['file'];
        $fileName           = $file->getClientOriginalName();
        $nameDoc            = $fileName;
        $request->file('file')->move("public/pdfpart/", $nameDoc);
        $tambah->dokumen    = $nameDoc;
        $tambah->save();

        return redirect('/dok_repo')->with('success', 'Created Repository Successfully!');
    }

    public function update(Request $request)
    {
    	$id_dok = $request['edit_id'];
    	$update = DokRepo::where('id_dokumen', $id_dok)->first();
    	$update->nama = $request['edit_nama_dok'];
    	$update->deskripsi = $request['edit_deskripsi'];
    	$update->update();

    	return redirect('/dok_repo')->with('update', 'Update Successfully!');
    }

